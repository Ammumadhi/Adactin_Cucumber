package com.java.org;

public class SwitchCase {
	
	public static void main(String[] args) {
		
		int i=10;
		switch (i) {
		case 1:
			System.out.println("Monday");
			break;
			 
	     case 10:
	    	 System.out.println("Tuesday");	    
	    	 break;

		default:
			System.out.println("Invalid");
			break;
		}
	}

}
